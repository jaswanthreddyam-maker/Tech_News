const { execSync } = require('child_process');
const fs = require('fs');

console.log("=== Running Phase 6I Part 2: Operational Excellence Certification ===\n");

function runCommand(command: string, name: string): string {
  console.log(`[ ] Running ${name}...`);
  try {
    const output = execSync(command, { encoding: 'utf8', stdio: ['ignore', 'pipe', 'pipe'] });
    console.log(`[✔] ${name} passed.\n`);
    return output;
  } catch (error: any) {
    console.error(`[X] ${name} failed. Aborting certification.\n`);
    if (error.stdout) console.log(error.stdout);
    if (error.stderr) console.error(error.stderr);
    process.exit(1);
  }
}

function checkFileExists(path: string, name: string) {
  if (fs.existsSync(path)) {
    console.log(`[✔] Found ${name}: ${path}`);
  } else {
    console.error(`[X] Missing ${name}: ${path}`);
    process.exit(1);
  }
}

function checkCommandRegistry() {
  console.log("[ ] Validating Command Registry...");
  const registryPath = 'src/lib/commands/registry.tsx';
  if (!fs.existsSync(registryPath)) {
    console.error(`[X] Missing Command Registry: ${registryPath}`);
    process.exit(1);
  }
  
  const content = fs.readFileSync(registryPath, 'utf8');
  
  const idMatches = [...content.matchAll(/id:\s*["']([^"']+)["']/g)];
  const ids = idMatches.map(m => m[1]);
  const duplicateIds = ids.filter((item, index) => ids.indexOf(item) !== index);
  if (duplicateIds.length > 0) {
    console.error(`[X] Found duplicate Command IDs: ${duplicateIds.join(', ')}`);
    process.exit(1);
  }

  const shortcutMatches = [...content.matchAll(/shortcut:\s*\[(["'][^"']+["']\s*(?:,\s*["'][^"']+["'])*)\]/g)];
  const shortcuts = shortcutMatches.map(m => m[1].replace(/["']/g, '').trim());
  const duplicateShortcuts = shortcuts.filter((item, index) => shortcuts.indexOf(item) !== index);
  if (duplicateShortcuts.length > 0) {
    console.error(`[X] Found duplicate Command Shortcuts: ${duplicateShortcuts.join(', ')}`);
    process.exit(1);
  }

  console.log(`[✔] Command Registry Validated. Found ${ids.length} unique commands.`);
}

console.log("1. Checking Required Providers & Configuration...");
checkFileExists('src/components/providers/TelemetryProvider.tsx', 'Telemetry Provider');
