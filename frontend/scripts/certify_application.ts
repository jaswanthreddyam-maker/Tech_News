checkFileExists('public/manifest.json', 'PWA Manifest');
checkFileExists('public/icon-192.png', 'PWA Icons');

console.log("\n2. Checking Architecture Requirements...");
checkCommandRegistry();

console.log("\n3. Running Typecheck...");
runCommand('npm run typecheck', 'TypeScript Compilation');

console.log("\n4. Running Linter...");
runCommand('npm run lint', 'ESLint');

console.log("\n5. Checking Build and Bundle Sizes...");
const buildOutput = runCommand('npm run build', 'Next.js Build');

// PWA Verification
checkFileExists('public/sw.js', 'Service Worker (Generated by Build)');

// Bundle Intelligence Configuration (in KB)
const bundleBudgets: Record<string, number> = {
  "/": 200,
  "/articles/[slug]": 250,
  "/dashboard": 250,
  "/search": 250
};

console.log("\n6. Validating Bundle Budgets...");
const lines = buildOutput.split('\n');
let budgetFailed = false;

for (const line of lines) {
  // Typical output line: "○ /   184 B   180 kB" or "ƒ /search   0 B   180 kB"
  // We want to capture the route and the "First Load JS" size (which is usually the last column)
  const match = line.match(/(?:○|ƒ|●|λ)\s+(.+?)\s+(?:\d+(?:\.\d+)?\s+[a-zA-Z]+)\s+(\d+(?:\.\d+)?)\s+([a-zA-Z]+)/);
  if (match) {
    const route = match[1].trim();
    const size = parseFloat(match[2]);
    const unit = match[3].toLowerCase();
    
    // Normalize size to KB
    let sizeInKb = size;
    if (unit === 'mb') sizeInKb = size * 1024;
    else if (unit === 'b') sizeInKb = size / 1024;

    // Check against budgets
    if (bundleBudgets[route]) {
      const budget = bundleBudgets[route];
      if (sizeInKb > budget) {
        console.error(`[X] Bundle Budget Exceeded: ${route} is ${sizeInKb.toFixed(2)} KB (Limit: ${budget} KB)`);
        budgetFailed = true;
      } else {
        console.log(`[✔] ${route}: ${sizeInKb.toFixed(2)} KB (Limit: ${budget} KB)`);
      }
    }
  }
}

if (budgetFailed) {
  console.error(`\n[X] Bundle Size Check Failed. Please optimize dependencies.`);
  process.exit(1);
}

console.log("\n=== Phase 6I Part 2 Certification Passed! ===");
console.log("Operational Excellence standards achieved.");

